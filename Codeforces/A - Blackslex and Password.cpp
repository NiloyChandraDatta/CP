// https://codeforces.com/contest/2179/problem/A
#include <bits/stdc++.h>
using namespace std;
#define ll long long

void solve()
{
    int k, x; cin>>k>>x;
    cout << k * x+1 << "\n";
}

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tc; cin >> tc;
    while (tc--)
    {
        solve();
    }
    return 0;
}
